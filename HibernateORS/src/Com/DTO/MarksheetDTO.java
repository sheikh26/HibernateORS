package Com.DTO;

public class MarksheetDTO {
private int RollNo;
private String Name;
private int phy;
private int che;
private int maths;
public int getRollNo() {
	return RollNo;
}
public void setRollNo(int rollNo) {
	RollNo = rollNo;
}
public String getName() {
	return Name;
}
public void setName(String name) {
	Name = name;
}
public int getPhy() {
	return phy;
}
public void setPhy(int phy) {
	this.phy = phy;
}
public int getChe() {
	return che;
}
public void setChe(int che) {
	this.che = che;
}
public int getMaths() {
	return maths;
}
public void setMaths(int maths) {
	this.maths = maths;
}

}
